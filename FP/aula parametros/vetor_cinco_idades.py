# criar um vetor com 5 idades

idades = [18, 21, 25, 12, 40]

# mostrar todos os valores
print ("Vetor completo: ", idades)

# aceder o 3º valor
print("Terceira idade: ", idades[2])

# alterar o valor do indice 1
idades[1] = 22
print("Depois da alteração: ", idades)

# percorrer o vetor com um loop
print("\nListar idades:")
for idade in idades:
    print(idade)
